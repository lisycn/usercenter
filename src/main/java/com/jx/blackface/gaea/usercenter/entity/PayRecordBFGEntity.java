package com.jx.blackface.gaea.usercenter.entity;

import com.bj58.sfft.utility.dao.annotation.Column;
import com.bj58.sfft.utility.dao.annotation.Id;
import com.bj58.sfft.utility.dao.annotation.Table;
import com.jx.spat.gaea.serializer.component.annotation.GaeaMember;
import com.jx.spat.gaea.serializer.component.annotation.GaeaSerializable;

@GaeaSerializable
@Table(name = "t_pay_record")
public class PayRecordBFGEntity {
	@GaeaMember
	@Id(insertable = true)
	@Column(name = "payid")
	private long payid;
	@GaeaMember
	@Column(name = "orderid")
	private long orderid;
	@GaeaMember
	@Column(name = "returncode")
	private String returncode;
	@GaeaMember
	@Column(name = "resultcode")
	private String resultcode;
	@GaeaMember
	@Column(name = "returnmsg")
	private String returnmsg;
	@GaeaMember
	@Column(name = "reqtime")
	private long reqtime;
	@GaeaMember
	@Column(name = "prepaystate")
	private int prepaystate;
	@GaeaMember
	@Column(name = "totalfee")
	private int totalfee;
	@GaeaMember
	@Column(name = "syschannel")
	private int syschannel;
	
	public int getSyschannel() {
		return syschannel;
	}
	public void setSyschannel(int syschannel) {
		this.syschannel = syschannel;
	}
	public long getPayid() {
		return payid;
	}
	public void setPayid(long payid) {
		this.payid = payid;
	}
	public long getOrderid() {
		return orderid;
	}
	public void setOrderid(long orderid) {
		this.orderid = orderid;
	}
	public String getReturncode() {
		return returncode;
	}
	public void setReturncode(String returncode) {
		this.returncode = returncode;
	}
	public String getResultcode() {
		return resultcode;
	}
	public void setResultcode(String resultcode) {
		this.resultcode = resultcode;
	}
	public String getReturnmsg() {
		return returnmsg;
	}
	public void setReturnmsg(String returnmsg) {
		this.returnmsg = returnmsg;
	}
	public long getReqtime() {
		return reqtime;
	}
	public void setReqtime(long reqtime) {
		this.reqtime = reqtime;
	}
	public int getPrepaystate() {
		return prepaystate;
	}
	public void setPrepaystate(int prepaystate) {
		this.prepaystate = prepaystate;
	}
	public int getTotalfee() {
		return totalfee;
	}
	public void setTotalfee(int totalfee) {
		this.totalfee = totalfee;
	}
	
	
}
